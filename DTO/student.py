class StudentDTO:
    def __init__(self, subject_id, year, semester, class_name, id_student, name, gender, dob, email, phone, address, teacher, photo):
        self.subject_id = subject_id
        self.year = year
        self.semester = semester
        self.class_name = class_name
        self.id_student = id_student
        self.name = name
        self.gender = gender
        self.dob = dob
        self.email = email
        self.phone = phone
        self.address = address
        self.teacher = teacher
        self.photo = photo